            <h4 class="deptcont line">Recent Projects</h4><br>
            <ul style="list-style-type:none;">
              <li class="dept-style"><span style="font-weight:400;">Optimizing C compiler for .NET</span><br/> 
              The aim of the project is to implement a reasonable and retargetable optimizing compiler. This compiler is meant to be used along with the "ROTOR" framework and will provide for a C compiler that outputs MSIL (Microsoft Intermediate Language).</li><br>
              <li class="dept-style"><span style="font-weight:400;">BIOCLUSTERS- Role of Cluster Computing in Bioinformatics</span><br/> 
              The project looks into the prospects of parallelising two major applications in bioinformatics on Beowulf clusters. These applications are chosen to be representatives of two problems groups that involve performing either large number of non computer intensive tasks (eg: performing BLAST searchs in database) or, a single task requiring huge computational power (eg: Protein folding simulation).</li><br>
              <li class="dept-style"><span style="font-weight:400;">32 Bit Operating System</span><br/> 
              The project is an attempt to build a 32 bit protected mode operating system based on the UNIX Kernel architecture.</li><br>
              <li class="dept-style"><span style="font-weight:400;">High Performance Cluster Computing - "SRISHTI" </span><br/> 
              It aims to demonstrate clusters as a viable alternative to MPPS (Massively Parallel Processors). It explores alternatives to TCP/IP in the form of GAMMA allowing a drastic reduction in network latency by eliminating a lot of the protocol overhead. It provides the basic framework for development of parallel programs based on message passing architecture. The ease of application development on the cluster is further enhanced by the indigenous Object Oriented Master Slave Library, MSLIB.</li><br> 
              <li class="dept-style"><span style="font-weight:400;">Spam Filtering Using Spam Mail Communities </span><br/>
              This project describes an approach towards spam filtering that seeks to exploit the nature of spam messages that allow them to be classified into different communities.</li><br>
              <li class="dept-style"><span style="font-weight:400;">Specialization Scores For Asymmetric Web Search Queries </span><br/>
             This is an approach to compute the specialization score for search queries, the aim of the score being to indicate how specialized the narrow topic is, to the broader topic.The approach generates scores for such queries, which can further be used to refine web searches for such queries, provided a given query could be identified to be of the form that we deal with.</li><br>
              <li class="dept-style"><span style="font-weight:400;">Context Disambiguation in Web Search Results </span><br/>
                 Web searches results in many pages that are not of interest, these may be due to a word or words in the search query having different contexts, the user obviously expecting to find pages related to the context of interest. This work proposes a method for disambiguating contexts in web search results.
</li><br>
          
            </ul><br/>

