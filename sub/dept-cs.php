            <h4 class="deptcont line">Aim</h4><br>
            <p>The foremost objective of the Computer Science and Engineering programme is to hone technical skills demanded by today's engineering professionals by providing a sound technical platform and the required knowledge base. It provides specialization in a wide range of hardware and software subjects in order to bridge the gap between knowledge and its practical application. Thus, the course produces an ideal software engineer, well equipped to meet the challenges faced by today's IT professional by exposing them to a wide array of cutting-edge technologies.</p>
            <p>The Undergraduate Computer Science and Engineering programme of Model Engineering College is ranked among the best technical programs in the country. The department offers a four year course at the end of which you receive a Bachelor of Technology degree in Computer Science & Engineering from Cochin University of Science and Technology.</p>
            <p>The program imparts an outstanding educational opportunity for those planning to pursue a career or to gain in-depth knowledge in computing technology and research. Keeping in pace with the IT era, the Computer Engineering syllabus covers an exhaustive realm of core fields like Operating Systems, Compiler Design, Finite Automata Theory, and Computer Graphics, etc.</p>
            <p>The Computer Labs are distinguished along the lines of the Platforms and Operating Systems used. There is a general Computing Lab for beginners, a UNIX Lab with scores of terminals and finally an Advanced Computing Lab that houses some of the best Hardware and software in the Industry.</p>

            <h4 class="deptcont line">Objectives</h4><br>
            <ul>
              <li class="dept-style">To provide every student with a strong conceptual foundation in the core areas of Computer Science.
              </li>
              <li class="dept-style">To enable students to engineer ingenious computing hardware and software solutions with the greatest degree of accuracy, flexibility and based on the latest engineering paradigms.</li>
              <li class="dept-style">To familiarize students with the latest in software and programming tools.</li>
              <li class="dept-style">To provide the greatest amount of pragmatism in approaching an engineering solution.</li>
              <li class="dept-style">To provide students with hands-on engineering experience through laboratory courses and project.</li>
              <li class="dept-style">    To coach students to work in teams and provide insight and direction into the atmosphere as might be encountered by professionals in the field.

</li>
